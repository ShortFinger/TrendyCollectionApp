/**
 * 从 modood 格式 JSON 生成可随 uni-app/Node 直接 import 的 `regionBundle.js`。
 * 在更新 `utils/data/*.json` 后执行：`node scripts/bundle-region.mjs`
 */
import { readFileSync, writeFileSync } from 'fs'
import { dirname, join } from 'path'
import { fileURLToPath } from 'url'

const root = join(dirname(fileURLToPath(import.meta.url)), '..')
const dataDir = join(root, 'utils', 'data')
const provinces = JSON.parse(readFileSync(join(dataDir, 'provinces.json'), 'utf8'))
const cities = JSON.parse(readFileSync(join(dataDir, 'cities.json'), 'utf8'))
const areas = JSON.parse(readFileSync(join(dataDir, 'areas.json'), 'utf8'))

const out = `// Auto-generated by scripts/bundle-region.mjs — do not edit by hand
export const provincesData = ${JSON.stringify(provinces)}
export const citiesData = ${JSON.stringify(cities)}
export const areasData = ${JSON.stringify(areas)}
`
const dest = join(dataDir, 'regionBundle.js')
writeFileSync(dest, out, 'utf8')
// eslint-disable-next-line no-console
console.log('Wrote', dest, `(${(out.length / 1024).toFixed(1)} KB)`)
